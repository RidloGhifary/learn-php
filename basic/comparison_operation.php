<?php

// == true if both has same value, converting data type
// === true is both has the same value and data type
// != true if one of it does not have the same value
// !== true if one of it does not have the same value and the same data type
// < true if first value less than second value
// <= true if first value less or same with second value
// > true if first value more than second value
// >= true if first value more or same with second value

var_dump(10 == "10");
var_dump(10 === "10");
var_dump(10 < "10");
var_dump(10 <= "10");